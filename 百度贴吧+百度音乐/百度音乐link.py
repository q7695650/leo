def getsonglist(i):

    url='http://music.baidu.com/songlist/tag/%E5%85%A8%E9%83%A8?orderType=1&offset='+str(i)
    response=requests.get(url=url,headers=headers).content
    response=etree.HTML(response)
    response=response.xpath('//div[@class="songlist-list"]//li')
    for i in response:
        link=i.xpath('.//p[@class="text-title"]/a/@href')[0]
        name=i.xpath('.//p[@class="text-title"]/a/@title')[0]
        num=i.xpath('.//span/text()')[0]
        data = {
            "title":name,
            "link":link,
            "number":num
        }
        collection.insert_one(data)
if __name__ == '__main__':
   pool=Pool(processes=4)
